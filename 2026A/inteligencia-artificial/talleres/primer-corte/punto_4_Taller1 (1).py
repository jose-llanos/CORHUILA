# Este programa implementa un modelo de aprendizaje automático llamado Perceptrón.
# El perceptrón se usa para clasificación binaria (0 o 1). El código:
# 1. Define la clase Perceptron.
# 2. Genera dos conjuntos de datos (uno con la compuerta lógica AND y otro con datos gaussianos separables).
# 3. Entrena el perceptrón con esos datos.
