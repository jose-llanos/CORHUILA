predicciones = modelo.forward(X)  # Calcula las predicciones reales (sin redondear)

plt.figure()  # Nueva figura
plt.plot(predicciones, 'o', label="Predicho")  # Grafica predicciones
plt.plot(y, 'x', label="Real")  # Grafica valores reales
