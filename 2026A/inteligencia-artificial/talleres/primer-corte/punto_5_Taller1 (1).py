# Este programa implementa una Red Neuronal Artificial llamada MLP (Multi-Layer Perceptron).
# El objetivo es aprender el problema l¿«gico XOR, que no puede resolverse con un perceptr¿«n simple.
# El programa:
# 1. Define funciones de activaci¿«n (sigmoid) y su derivada.
# 2. Crea una clase MLP con propagaci¿«n hacia adelante (forward) y hacia atr¿ós (backpropagation).
# 3. Entrena la red neuronal con el dataset XOR.
# 4. Muestra las predicciones del modelo.
# 5. Grafica:
#    - La evoluci¿«n del error durante el entrenamiento
#    - La frontera de decisi¿«n
#    - Comparaci¿«n entre valores reales y predichos.

import numpy as np  # Importa NumPy para operaciones matem¿óticas y manejo de matrices
import matplotlib.pyplot as plt  # Importa Matplotlib para crear gr¿óficos

np.random.seed(42)  # Fija la semilla aleatoria para que los resultados sean reproducibles


def sigmoid(z):  # Define la funci¿«n de activaci¿«n sigmoide
    return 1 / (1 + np.exp(-z))  # Aplica la f¿«rmula de la sigmoide


def sigmoid_derivative(a):  # Define la derivada de la funci¿«n sigmoide
    return a * (1 - a)  # F¿«rmula de la derivada usando la salida de la sigmoide


class MLP:  # Define la clase de la red neuronal Multilayer Perceptron

    def __init__(self, layers, learning_rate=0.1, epochs=10000):
        # Constructor de la red neuronal

        self.layers = layers  # Lista que indica el n¿▓mero de neuronas por capa
        self.learning_rate = learning_rate  # Tasa de aprendizaje
        self.epochs = epochs  # N¿▓mero de ¿Ğpocas de entrenamiento

        self.weights = []  # Lista para guardar matrices de pesos
        self.biases = []  # Lista para guardar los bias de cada capa
        self.loss_history = []   # Lista para guardar la evoluci¿«n del error

        # Inicializaci¿«n din¿ómica de pesos y bias
        for i in range(len(layers)-1):  # Recorre cada par de capas
            self.weights.append(np.random.randn(layers[i], layers[i+1]))
            # Crea una matriz de pesos aleatoria entre la capa actual y la siguiente

            self.biases.append(np.zeros((1, layers[i+1])))
            # Inicializa los bias en cero


    def forward(self, X):  # Propagaci¿«n hacia adelante (forward propagation)

        self.activations = [X]  # Guarda la activaci¿«n de la capa de entrada

        for i in range(len(self.weights)):  # Recorre todas las capas
            z = np.dot(self.activations[-1], self.weights[i]) + self.biases[i]
            # Calcula la combinaci¿«n lineal (entrada * pesos + bias)

            a = sigmoid(z)  # Aplica funci¿«n de activaci¿«n
            self.activations.append(a)  # Guarda la activaci¿«n de esta capa

        return self.activations[-1]  # Devuelve la salida final de la red


    def backward(self, X, y):  # Retropropagaci¿«n del error (Backpropagation)

        m = X.shape[0]  # N¿▓mero de muestras
        deltas = []  # Lista para guardar los gradientes de error

        # Error en la capa de salida
        error = self.activations[-1] - y  # Diferencia entre predicci¿«n y valor real

        delta = error * sigmoid_derivative(self.activations[-1])
        # Gradiente del error multiplicado por derivada de la sigmoide

        deltas.append(delta)  # Guarda el delta de la capa de salida

        # Propagaci¿«n del error hacia atr¿ós
        for i in reversed(range(len(self.weights)-1)):  # Recorre capas en orden inverso
            error = np.dot(deltas[-1], self.weights[i+1].T)
            # Propaga el error hacia la capa anterior

            delta = error * sigmoid_derivative(self.activations[i+1])
            # Calcula el nuevo delta

            deltas.append(delta)  # Guarda el delta

        deltas.reverse()  # Invierte el orden para coincidir con las capas

        # Actualizaci¿«n de pesos y bias
        for i in range(len(self.weights)):  # Recorre cada capa
            self.weights[i] -= self.learning_rate * np.dot(self.activations[i].T, deltas[i]) / m
            # Ajusta los pesos usando gradiente descendente

            self.biases[i] -= self.learning_rate * np.sum(deltas[i], axis=0, keepdims=True) / m
            # Ajusta los bias


    def fit(self, X, y):  # Funci¿«n para entrenar la red

        for epoch in range(self.epochs):  # Recorre todas las ¿Ğpocas
            output = self.forward(X)  # Realiza propagaci¿«n hacia adelante
            self.backward(X, y)  # Ajusta los pesos con backpropagation

            loss = np.mean((y - output) ** 2)  # Calcula el error cuadr¿ótico medio (MSE)
            self.loss_history.append(loss)  # Guarda el error en el historial

            if epoch % 1000 == 0:  # Cada 1000 ¿Ğpocas
                print(f"Epoch {epoch}, Loss: {loss}")  # Imprime el error


    def predict(self, X):  # Funci¿«n para realizar predicciones
        output = self.forward(X)  # Calcula la salida de la red
        return np.round(output)  # Redondea a 0 o 1


# -------------------------
# DATOS XOR
# -------------------------

X = np.array([[0,0],
              [0,1],
              [1,0],
              [1,1]])  # Entradas posibles del problema XOR

y = np.array([[0],
              [1],
              [1],
              [0]])  # Salidas del problema XOR


modelo = MLP(
    layers=[2, 8, 1],  # 2 neuronas entrada, 8 ocultas, 1 salida
    learning_rate=0.1,  # Tasa de aprendizaje
    epochs=10000  # N¿▓mero de ¿Ğpocas
)

modelo.fit(X, y)  # Entrena el modelo con los datos XOR

print("Predicciones XOR:")  # Muestra texto en consola
print(modelo.predict(X))  # Imprime predicciones del modelo


# ===============================
# 1. GR¿óFICA DE ERROR
# ===============================

plt.figure()  # Crea una nueva figura
plt.plot(modelo.loss_history)  # Grafica la evoluci¿«n del error
plt.title("Evoluci¿«n del Error (Loss)")  # T¿¬tulo del gr¿ófico
plt.xlabel("¿Ğpocas")  # Etiqueta eje X
plt.ylabel("MSE")  # Etiqueta eje Y
plt.show()  # Muestra la gr¿ófica


# ===============================
# 2. FRONTERA DE DECISI¿«N
# ===============================

x_min, x_max = -0.5, 1.5  # L¿¬mites del eje X
y_min, y_max = -0.5, 1.5  # L¿¬mites del eje Y

xx, yy = np.meshgrid(np.linspace(x_min, x_max, 200),
                     np.linspace(y_min, y_max, 200))
# Crea una malla de puntos para evaluar el modelo

grid = np.c_[xx.ravel(), yy.ravel()]  # Convierte la malla en lista de puntos

Z = modelo.predict(grid)  # Predice la clase para cada punto

Z = Z.reshape(xx.shape)  # Reorganiza las predicciones a la forma de la malla

plt.figure()  # Nueva figura
plt.contourf(xx, yy, Z, alpha=0.3)  # Dibuja regiones de decisi¿«n
plt.scatter(X[:,0], X[:,1], c=y.flatten())  # Grafica los puntos del dataset
plt.title("Frontera de Decisi¿«n - XOR")  # T¿¬tulo
plt.xlabel("X1")  # Etiqueta eje X
plt.ylabel("X2")  # Etiqueta eje Y
plt.show()  # Muestra la gr¿ófica


